int		ft_isalpha(int c)
{
	unsigned char	d;

	if (c < 0 || c >= 255)
		return (0);
	d = (unsigned char)c;
	return ((d >= 'A' && d <= 'Z') || (d >= 'a' && d <= 'z') ? 1 : 0);
}
