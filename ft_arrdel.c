#include "libft.h"
#include <stdlib.h>

void	ft_arrdel(char **arr)
{
	size_t	i;

	i = 0;
	while (arr[i])
	{
		ft_memdel((void **)arr[i]);
		i++;
	}
	free(arr);
}
